{- String -}
nome = "Daniel"

{- Soma -}
soma a b = a + b

{- Subtração -}
sub a b = a - b

{- Multiplicação -}
mult a b = a * b

{- Divisão --> resultado na forma de um flaot -}
divisao a b = a / b

{- Potenciação -}
potencia a b = a ** b

{- Raiz quadrada --> resultado na forma de um float -}
raiz2 a = sqrt a

{- Parênteses são usados apenas para definir uma expressao q na real é apenas um parâmetro -}
hipotenusa b c = raiz2 ( potencia b 2 + potencia c 2)