import math

#teto
print(math.ceil(10.5))

#piso
print(math.floor(10.5))

#fatorial
print(math.factorial(5))

#potencia
print(math.pow(2,5))#float
print(2**5)#int

#raiz quadrada
print(math.sqrt(25))

#mdc
print(math.gcd(10,5))

